    private void bagisciEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bagisciEkleActionPerformed
        String adA = ad.getText();
        String soyadA = soyad.getText();
        String bagıs_türüA = bagıs_türü.getText();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        String tarihA = dateFormat.format(tarih.getDate());
        String saatA = saat.getText();

        PreparedStatement st;
        ResultSet rs;
        String sorgu = "INSERT INTO randevu (ad,soyad,bagıs_türü,tarih,saat) VALUES (?,?,?,?,?)";
        try {
            st = KanBagis.getConnection().prepareStatement(sorgu);
            st.setString(1, adA);
            st.setString(2, soyadA);
            st.setString(3, bagıs_türüA);
            st.setString(4, tarihA);
            st.setString(5, saatA);

            if (st.executeUpdate() != 0) {
                JOptionPane.showMessageDialog(null, "Randevu Oluşturuldu");
            } else {
                JOptionPane.showMessageDialog(null, "Randevu Oluşturulmadı");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_bagisciEkleActionPerformed

