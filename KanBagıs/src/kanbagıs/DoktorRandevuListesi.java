    public ArrayList<Randevu> getRandevu() throws SQLException {
        Statement st;
        ResultSet rs;
        ArrayList<Randevu> randevular= null;
        try{
           st = KanBagis.getConnection().createStatement();
           rs = st.executeQuery("select * from randevu");
           randevular = new ArrayList<Randevu>();
           while(rs.next()){
               randevular.add(new Randevu(
                       rs.getString("ad"),
                       rs.getString("soyad"),
                         rs.getString("bagıs_türü"),
                      rs.getString("tarih"),
                       rs.getString("saat")
               ));
           }
            
        }catch(Exception e){
        e.printStackTrace();
    }
        return randevular;
    }
